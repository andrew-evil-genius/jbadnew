<h4>Sorting</h4>
<p>To sort the lead list, click on the column name. 
The small triangle indicates whether you are sorting 
ascending or descending values.</p>
<h4>Editing</h4>
<p>Double click on the row of the lead you would 
like to edit.</p>
<h4>Filtering</h4>
<p>The basic filter allows you to select a column and 
then filter the results based on the text entered into 
the search box.  Clicking on the advanced filter link 
allows you to do the same thing but on multiple columns 
at a time.</p>